									<b>ＫＰＭとは?</b><br>
									☞ ＤＰＲＫ Ｋｏｒｅａ 
									Ｍｅｄｉａの略&#31216;で、インタ&#12540;ネットを介した朝鮮民主主義人民共和&#22269;のリアルタイム記事送信網であり、また引用&#36578;載&#27177;/機&#38306;全者&#38322;&#35239;&#27177;管理を運&#21942;するサイトです。共和&#22269;&#38306;係の報道記事の無&#26029;不法利用や&#27584;損を防ぎ相&#24540;な料金を請求するために開設した、朝鮮言論情報基地です。<a id="2"></a><br>
									<br>
									<b>ＫＰＭはどこが運&#21942;しているのか?</b><br>
									☞ 
									東京にある&#12849;コリアメディアで運&#21942;しています。２００５年６月に開設されました。コリアメディアは朝鮮民主主義人民共和&#22269;の記事引用&#36578;載&#27177;/全者&#38322;&#35239;&#27177;管理窓口の企業として共和&#22269;の公式委任と&#29420;占契約により全世界を&#23550;象に&#21942;業と管理業務をおこないます。<a id="3"></a><br>
									<br>
									<b>ＫＰＭはどのような媒&#20307;の記事を送信するのか?</b><br>
									☞ 
									2005年12月現在、６つの新聞/15種定期刊行物の紙面全記事及び資料を送信します。日刊新聞として「&#21172;&#20685;新聞」、「民主朝鮮」、「朝鮮新報平&#22732;支局」、週間新聞として「統一新報」、「文&#23398;新聞」、「ピョンヤンタイムス」（英字版）、月刊誌として「千里馬」、「朝鮮文&#23398;」、「&#38738;年文&#23398;」、「&#20816;童文&#23398;」、「朝鮮芸術」、季刊誌として「&#27508;史科&#23398;」、「&#32076;&#28168;&#30740;究」、「政治法律&#30740;究」、「哲&#23398;&#30740;究」、「朝鮮語文」、「民族文化遺産」、「文化語&#23398;習」、「金日成綜合大&#23398;&#23398;報(哲&#23398;、&#32076;&#28168;)」、「金日成綜合大&#23398;&#23398;報(&#27508;史、法&#23398;)」、「金日成綜合大&#23398;&#23398;報(語文&#23398;)」の全記事と資料を送信します。2006年からは「朝鮮中央年鑑」「朝鮮文&#23398;芸術年鑑」などの送信サ&#12540;ビスを行なう予定であり、その他コンテンツも引き&#32154;き追加、送信する予定です。<a id="4"></a><br>
									<br>
									<b>紙面記事以外にどのような記事を送信するのか？</b><br>
									紙面に&#25522;載されない「&#21172;&#20685;新聞」取材記事や社&#20250;科&#23398;院、金日成綜合大&#23398;などのアカデミ&#12540;機&#38306;から提供される寄稿文、深層開設記事、論文などを送信します。<a id="5"></a><br>
									<br>
									<b>記事更新速度と時間&#24111;は?</b><br>
									☞ 
									&#24403;日&#30330;刊される新聞のすべての紙面記事は&#27598;日、6時から12時の間にインタ-ネットで配信します。「朝鮮新報」平&#22732;支局取材記者からの記事、論評、寄稿、&#38306;連&#20889;&#30495;などはリアルタイムで&#38543;時に更新します。週間新聞及び&#38609;誌についても&#30330;刊&#24403;日24時間以&#20869;に記事全量を配信する予定です。<a id="6"></a><br>
									<br>
									<b>一日配信記事/&#20889;&#30495;の量は？</b><br>
									☞ 2006年1月現在で一日平均配信情報件&#25968;は朝鮮語文/100件、日語文10件、&#20889;&#30495;5件以上をめどにしています。<a id="7"></a><br>
									<br>
									<b>記事全文は誰でも&#38322;&#35239;することができるのか?</b><br>
									☞ 
									コリアメディアと引用転載権の公式契約を締結した言論機関など法人機関に限って閲覧することができます。個人を対象にした記事閲覧サービスは扱いません。<a id="8"></a><br>
									<br>
									<b>有料か無料か?</b><br>
									☞ 有料です。契約期間は機&#38306;別1年&#21336;位で締結します。<a id="9"></a><br>
									<br>
									<b>送受信システムは？</b><br>
									☞ 記事提供機関（平壌）→ 逓信省（平壌）→ ＫＰＭ（東京）を通じてリアルタイムで送信され契約社では固定ＩＰが登録された該当部署内のコンピューターで同時に閲覧できます。<a id="14"></a><br>
									<br>
									<b>記事の持&#32154;的提供と送信の安定性を保&#35388;できるのか？</b><br>
									☞ 
									ＫＰＭは朝鮮民主主義人民共和&#22269;の公式委任と在日朝鮮人&#32207;連合&#20250;中央常任委員&#20250;機&#38306;誌を&#30330;行する朝鮮新報社の全面的な協調提携を受ける事業です。よってその社&#20250;的な保障性の面においては背景がしっかりしていると言えます。また、停電やハッカ&#12540;の侵入などに&#23550;&#20966;して大手のサ&#12540;バ&#12540;ホスティング社との提携の下、&#24375;力なファイア&#12540;ウォ&#12540;ルを備えた送受信システムを設置運&#21942;しているのでスピ&#12540;ドと安定性を保&#35388;できます。万一、送信中&#26029;などの事態が生じたとしても、契約書において還給に&#38306;する項目が設けてあるので問題はないと思われます。<a id="10"></a><br>
									<br>
									<b>朝鮮中央通信社との違いは何か？</b><br>
									☞ 
									朝鮮中央通信社は共和&#22269;政府の政策的立場を代弁する&#22768;明/談話/論評を公式的に世界に向けて&#30330;信する&#22269;家機&#38306;です。よって各メディアの記事著作&#27177;の管理及び代理業務は行なわないし、メディア、記事、送信の業務も扱いません。コリアメディアは共和&#22269;メディアの委任を受け、記事著作&#27177;事業において提起される料金請求、契約など諸般の業務を行なう窓口の事業&#20307;としてＫＰＭインタ&#12540;ネットを通じてメディア、全記事、全文をリアルタイムで送信します。<a id="12"></a><br>
									<br>
									<b>朝鮮通信（朝鮮中央通信社）とは別途契約しなければならないのか？</b><br>
									☞ 
									特別な&#20869;外の懸案に&#38306;する共和&#22269;政府の公式立場は朝鮮中央通信の報道文という形式で&#30330;表されることが多&#12293;あります。共和&#22269;の新聞はこの報道を引用&#36578;載するか、&#38306;連記事を追加しながら紙面報道し、ＫＰＭをつうじても送信します。このように朝鮮中央通信は共和&#22269;の代弁機&#38306;という地位と役割に沿って&#22269;家次元の固有な報道領域を備えています。よってその報道&#20869;容を&#21363;刻、受けなければならない機&#38306;では別途の契約が必要です。<a id="13"></a><br>
